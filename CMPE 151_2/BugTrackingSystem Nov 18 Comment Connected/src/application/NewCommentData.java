package application;

public class NewCommentData {
	String comments_ID;
	String commentDescription;
	String commentDateTime;

	public NewCommentData(String comments_ID, String commentDescription, String commentDateTime) {
		super();
		this.comments_ID = comments_ID;
		this.commentDescription = commentDescription;
		this.commentDateTime = commentDateTime;
	}

	// Getter that associates with the comment.
	public String getComments_ID() {
		return comments_ID;
	}

	public String getCommentDescription() {
		return commentDescription;
	}

	public String getCommentDateTime() {
		return commentDateTime;
	}

	// Setter that associates with the comment.
	public void setComments_ID(String comments_ID) {
		this.comments_ID = comments_ID;
	}

	public void setCommentDescription(String commentDescription) {
		this.commentDescription = commentDescription;
	}

	public void setCommentDateTime(String commentDateTime) {
		this.commentDateTime = commentDateTime;
	}
}
