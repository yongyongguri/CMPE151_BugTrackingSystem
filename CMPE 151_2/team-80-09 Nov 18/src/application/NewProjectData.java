package application;

/**
 * NewProjectData class contains attributes such as project ID, project name,
 * Starting date, Project description, and project level.
 */
public class NewProjectData {

	String projectID;
	String newProjectName;
	String projectDate;
	String projectDescription;
	String projectLevel;

	// Getters for associated with the project.
	public String getProjectID() {
		return projectID;
	}

	public String getNewProjectName() {
		return newProjectName;
	}

	public String getProjectDate() {
		return projectDate;
	}

	public String getProjectDescription() {
		return projectDescription;
	}

	public String getProjectLevel() {
		return projectLevel;
	}

	// Setters for associated with the project.
	public void setProjectID(String projectID) {
		this.projectID = projectID;
	}

	public void setNewProjectName(String newProjectName) {
		this.newProjectName = newProjectName;
	}

	public void setProjectDate(String projectDate) {
		this.projectDate = projectDate;
	}

	public void setProjectDescription(String projectDescription) {
		this.projectDescription = projectDescription;
	}

	public void setProjectLevel(String projectLevel) {
		this.projectLevel = projectLevel;
	}

	// Constructs a NewProjectData objects with the project name, starting date,
	// project description, and project level.
	public NewProjectData(String newProjectName, String projectDate, String projectDescription, String projectLevel, String projectID) {
		super();
		this.newProjectName = newProjectName;
		this.projectDate = projectDate;
		this.projectDescription = projectDescription;
		this.projectLevel = projectLevel;
		this.projectID = projectID;
	}
}
